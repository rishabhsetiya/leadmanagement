create TABLE RESTAURANTS (
    ID INT PRIMARY KEY,
    NAME VARCHAR(255),
    ADDRESS VARCHAR(255),
    STATUS VARCHAR(255),
    FREQUENCY VARCHAR(255)
);

create TABLE ORDERS (
    ID INT PRIMARY KEY,
    RESTAURANT_ID INT,
    ORDER_TIME TIMESTAMP,
    AMOUNT DECIMAL(10,2),
    FOREIGN KEY (RESTAURANT_ID) REFERENCES RESTAURANTS(ID)
);

create TABLE CONTACTS (
    ID INT PRIMARY KEY,
    NAME VARCHAR(255),
    EMAIL VARCHAR(255),
    PHONE VARCHAR(255),
    RESTAURANT_ID INT,
    ROLE VARCHAR(255),
    FOREIGN KEY (RESTAURANT_ID) REFERENCES RESTAURANTS(ID)
);

create TABLE CALLS (
    CONTACT_ID INT,
    CALL_TIME TIMESTAMP,
    RESTAURANT_ID INT,
    FOREIGN KEY (CONTACT_ID) REFERENCES CONTACTS(ID),
    FOREIGN KEY (RESTAURANT_ID) REFERENCES RESTAURANTS(ID));

